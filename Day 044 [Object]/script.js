//Object 
// An object in JavaScript is a collection of key-value pairs, where each key (a string) maps to a value of any type, used to store data and methods.

let detail ={
    name:"John",
    age:30,
    occupation:"Software Engineer",
    gender:"male"
}

console.log(detail);
console.log(detail.name);
console.log(detail["age"]);
console.log(typeof detail); //object
