// Two types of Data type

// Primitive / Non- Reference / Immutable(main data not change only change copy data) 
// String, Number, Boolean, Null, Undefined
//Primitive Data types are predefined. 

// Non - primitive // Reference / mutable(it will change main data) 
// Object, Array, Function
//Non-Primitive data types are created by the programmer

let a=[1];
let b=a;
a[0]=111;
console.log(a,b); // [111][111]



